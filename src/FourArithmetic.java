public class FourArithmetic {
    public static void main (String[] args) {
        int a, b, sum, sub, mul, div;
        a = 12;
        b = 2;
        sum = a + b;
        sub = a - b;
        mul = a * b;
        div = a / b;
        System.out.println("a + b = " + sum);
        System.out.println("a - b = " + sub);
        System.out.println("a * b = " + mul);
        System.out.println("a / b = " + div);
    }
}
